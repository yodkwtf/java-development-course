public class Long {
  public static void main(String[] args) {
    long dailyGoogleSearches = 8000000000L;
    long population = 95_000_000_000L; // underscore does nothing but makes it easier to read
    System.out.println("Daily, there are about " + dailyGoogleSearches + " google searches!");
    System.out.println("The population is about " + population + ".");
  }
}
